function t = nl_transform(d)
    t = [ ...
        d(:,1) ...
        d(:,2) ...
        d(:,1).*d(:,1) ...
        d(:,1).*d(:,2) ...
        d(:,2).*d(:,2) ...
        d(:,1).*d(:,1).*d(:,1) ...
        d(:,1).*d(:,1).*d(:,2) ...
        d(:,1).*d(:,2).*d(:,2) ...
        d(:,2).*d(:,2).*d(:,2) ...
    ];
end